def say_hello():
    """Returns Hello"""
    return "Hello"

def return_true():
    """Returns True"""
    return True

def return_false():
    """Returns False"""
    return False

def add_to(item, list):
    """Appends the item to a list"""
    list.append(item)
    return list

def remove_from(item, list):
    list.remove(item)
    return list
