my_list_4 = [4, 16, 5, 12, 69]

print("Assuming the following list:")
print(my_list_4)
print("")

print("The smallest number, using the min function, is:")
print(min(my_list_4))
print("")

print("The largest number, using the max function, is:")
print(max(my_list_4))
print("")

print("The sum of all values, using the sum function, is:")
print(sum(my_list_4))
print("")

print("The average of all values, performed manually with sum and len, is:")
print(sum(my_list_4)/len(my_list_4))
